    public static string GetMimeMapping(string fileName)
    {
      string mimeMapping = (string) null;
      if (!MimeMapping.sm_map.TryGetValue(Path.GetExtension(fileName), out mimeMapping))
        mimeMapping = MimeMapping.sm_map[".*"];
      return mimeMapping;
